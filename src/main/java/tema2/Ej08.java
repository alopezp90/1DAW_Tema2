public class Ej08 {
    public static void main(String[] args) {
        
        //Declaramos e inicializamos las variables
        int a=10, b=5, i=0;
        double c=71.3, d=4.8;
        
        //Declaramos e inicializamos las variales de las operaciones
        int e=a+b, f=a-b, g=a*b, h=a/b;
        double j=c+d, k=c-d, l=c*d, m=c/d;
        
        //Imprimimos por pantalla los resultados
        System.out.println("a+b = "+e);
        System.out.println("a-b = "+f);
        System.out.println("a*b = "+g);
        System.out.println("a/b = "+h);
        
        System.out.println("c+d = "+j);
        System.out.println("c-d = "+k);
        System.out.println("c*d = "+l);
        System.out.println("c/d = "+m);
        
        //Imprimimos por pantalla i, i++ y ++i
        System.out.println("i = "+i);
        System.out.println("i++ = "+(i++));
        System.out.println("++i = "+(++i));

 
    }
    
}
